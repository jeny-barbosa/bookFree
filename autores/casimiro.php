        <p>Casimiro José Marque de Abreu foi um importante poeta brasileiro da Segunda Geração Romântica. Nasceu na cidade de Barra de São João, atual Casimirana (Rio de Janeiro) em 1837.</p>
        <p>Em 1853, foi morar, junto com o pai, em Portugal, país onde escreveu grande parte de sua obra.</p>
        <p>Autor de “Primaveras”, coleção de poesias de caráter melancólico e sentimental. Utilizava uma grande simplicidade na forma de escrever, aliada a um sentimento exagerado e apaixonado. Suas poesias tratavam de temas relacionados à sua vida, à casa do pai, ao amor e à saudade da terra natal.
        </p>
        <p>Tuberculoso, morreu com apenas 23 anos, em 1860, numa fazenda nos arredores da cidade onde nasceu. Suas poesias fazem sucesso até hoje, principalmente, no Brasil e Portugal.
