        <p>Coelho Neto (1864-1934) foi escritor, político e professor brasileiro. Foi membro fundador da Academia Brasileira, ocupou a cadeira nº 2. Usou em sua obra um vocabulário cheio de artifícios retóricos. Escreveu mais de cem livros e aproximadamente 650 contos.</p>
        <p>Coelho Neto (1864-1934) nasceu em Caxias, Maranhão no dia 20 de fevereiro de 1864. Filho do português Antônio da Fonseca Coelho e da índia Ana Silvestre Coelho. Em 1870, a família foi morar no Rio de Janeiro. Foi aluno do Colégio Pedro II. Estudou Medicina e Direito, mas não concluiu nenhum dos cursos. Participava de movimentos abolicionistas e republicanos.</p>
        <p>Em 1885, conheceu José do Patrocínio, que o introduziu na redação do jornal Gazeta da Tarde e no periódico A Cidade do Rio. Em 1890, casa-se com Maria Gabriela Brandão, juntos tiveram quatorze filhos. Nesse mesmo ano ocupou a Secretaria do Governo do Estado do Rio de Janeiro. Em 1891, publicou sua primeira obra "Rapsódias", um livro de contos. Em 1892, lecionou História da Arte na Escola Nacional de Belas Artes e Literatura no Colégio Pedro II.</p>
        <p>Coelho Neto escreveu "Capital Federal: Impressões de um Sertanejo"(1893). Em 1896, participa das primeiras reuniões com objetivo de criar a Academia Brasileira de Letras. Publicou "Sertão" (1896) e "Álbum de Caliban"(1897), "O Paraíso"(1898) e "A Conquista"(1899). Foi eleito Deputado Federal pelo Maranhão, para a legislatura de 1909 e 1911. Foi nomeado para a cátedra de História do Teatro e para Literatura Dramática na Escola de Arte Dramática, em 1910.</p>
        <p>Escreveu algumas peças teatrais, mais de cem livros e cerca de 650 contos. Em 1928, foi consagrado como “Príncipe dos Prosadores Brasileiros”. De sua extensa obra literária, destacam-se também "Fruto Proibido", "O Rei Fantasma", "Contos Pátrios", "Mano", "As Estações", "Mistério do Natal" e “A Cidade Maravilhosa”. Também poeta, escreveu um soneto que se tornaria famoso "Ser Mãe".
          Henrique Maximiniano Coelho Neto morreu no Rio de Janeiro, no dia 28 de novembro de 1934.</p>
