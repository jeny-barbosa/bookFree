      <td>
        <p>Olavo Brás Martins dos Guimarães Bilac nasceu em dezembro de 1865 no Rio de Janeiro. Incentivado pelo pai cursou Medicina, mas abandonou o curso no último ano. Iniciou o curso de Direito em São Paulo, contudo, foi outra faculdade que não conseguiu finalizar. A partir de então, o escritor dedicou-se ao jornalismo e à literatura.</p>
        <p>Em 1896, foi um dos fundadores da Academia Brasileira de Letras. Envolveu-se com os preceitos republicanos e nacionalistas. É autor do “Hino à Bandeira” escrito em 1889. Por fazer sátiras ao governo de Floriano foi exilado em Ouro Preto, Minas Gerais.</p>
        <p>Em conjunto com Raimundo Correia e Alberto de Oliveira forma a tríade Parnasiana brasileira. É o autor mais popular do Parnasianismo e tem como princípios: a busca por perfeição na forma de escrever (versos alexandrinos); a exaltação pelos poemas épicos da Antiguidade Clássica (Ilíada), o forte lirismo; a inovação nos temas filosóficos de meditação; o descritivismo e nacionalismo, além de linguagem com conotação sensual, como é o caso do poema “Sarças de fogo”.
        </p>
        <p>Bilac teve uma vida voltada à participação na vida política, o que não o influenciou na subjetividade de sua literatura. Em sua militância, iniciou campanhas cívicas a favor da alfabetização e serviço militar obrigatório. Viveu uma vida solitária de muitas viagens à Europa, o que possivelmente o fez refletir sobre a realidade sócio-política do Brasil, ao comparar as transformações entre os países.
        </p>
        <p>O poeta faleceu em dezembro, dois dias depois de completar 53 anos, em 1918 na sua cidade natal.</p>
