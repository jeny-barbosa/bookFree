                <p>Suas poesias trazem marcantes sentimentos de pessimismo e desânimo, além de inclinação para a morte. Com relação à estrutura, pode-se dizer que suas poesias apresentam rigor na forma e rico conteúdo metafórico.
                </p>

                <p>Morreu ainda jovem (em 1914) devido a uma enfermidade pulmonar, deixando para trás suas carreiras de promotor público (formou-se em Direito em 1906) e de professor, além de sua única e marcante obra. </p>
