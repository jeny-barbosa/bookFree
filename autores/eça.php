      <td>
        <p>José Maria Eça de Queiroz foi um importante romancista português do século XIX. Nasceu em 25 de novembro, de 1845, na cidade portuguesa de Póvoa de Varzim.</p>
        <p>Aos 16 anos de idade, foi estudar Direito na cidade de Coimbra. Seus primeiros trabalhos como escritor apareceram no Jornal Gazeta de Portugal. Trabalhou como administrador municipal no município de Leiria. Trabalhou também como cônsul de Portugal na Inglaterra. Esta época foi uma das mais produtivas de sua carreira.</p>
        <p>Foi discípulo do escritor francês Gustave Flaubert, de quem recebeu grande influência literária. Eça de Queiroz foi um dos pioneiros da literatura realista em Portugal.</p>
        <p>Abordou, em suas obras, diversos temas. Porém, podemos observar algumas características comuns em seus romances, como, por exemplo, abordagem de temas cotidianos, descrição de locais e comportamento de pessoas, pessimismo, ironia e humor.
        </p>
        <p>Eça de Queiroz morreu na cidade de Paris em 1900. Suas obras foram traduzidas em várias línguas. É considerado até os dias de hoje como sendo um dos principais representantes do realismo português.</p>
